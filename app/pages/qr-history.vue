<script setup lang="ts">
import type { PaymentQrDetail, PaymentQrStatus, QrHistoryRecord } from '~/types'

definePageMeta({ middleware: 'auth' })

const router = useRouter()
const { paymentsApi } = useApi()

// â”€â”€ Filter tabs â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

const TABS = [
  { value: undefined,   label: 'All',      icon: 'ğŸ”²', countClass: 'bg-slate-600 text-gray-200' },
  { value: 'pending',   label: 'Pending',  icon: 'â³', countClass: 'bg-indigo-500/20 text-indigo-300' },
  { value: 'paid',      label: 'Paid',     icon: 'âœ…', countClass: 'bg-emerald-500/20 text-emerald-300' },
  { value: 'expired',   label: 'Expired',  icon: 'â°', countClass: 'bg-rose-500/20 text-rose-300' },
  { value: 'cancelled', label: 'Cancelled',icon: 'ğŸš«', countClass: 'bg-slate-600 text-gray-400' },
] as const

type TabValue = (typeof TABS)[number]['value']

const activeStatus = ref<TabValue>(undefined)

// Tab counts â€” loaded once for the "All" totals, then per-status lazily
const tabCounts = reactive<Record<string, number | undefined>>({})

// â”€â”€ Pagination â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

const skip  = ref(0)
const limit = 20

// â”€â”€ Data â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

const records   = ref<QrHistoryRecord[]>([])
const total     = ref(0)
const isLoading = ref(false)

async function load() {
  isLoading.value = true
  try {
    const params: Record<string, unknown> = { skip: skip.value, limit }
    if (activeStatus.value) params.status = activeStatus.value

    const result = await paymentsApi.getQrHistory(params)
    records.value = result.data
    total.value   = result.total

    // Update tab count for the active filter
    const key = activeStatus.value ?? 'all'
    tabCounts[key] = result.total
  } finally {
    isLoading.value = false
  }
}

function setStatus(val: TabValue) {
  activeStatus.value = val
  skip.value = 0
  void load()
}

function nextPage() {
  skip.value += limit
  void load()
}

function prevPage() {
  skip.value = Math.max(0, skip.value - limit)
  void load()
}

// â”€â”€ Live clock for relative durations â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

// Ticks every 30 seconds so "expires in X" / "expired X ago" stays fresh
const now = ref(Date.now())
let clockTimer: ReturnType<typeof setInterval> | null = null

// â”€â”€ Formatters â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

function shortId(id: string) {
  return `#${id.slice(-6).toUpperCase()}`
}

function formatAmount(n: number) {
  return new Intl.NumberFormat('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(n)
}

function formatDate(iso: string | null | undefined): string {
  if (!iso) return 'â€”'
  return new Date(iso).toLocaleString('en-US', { month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit' })
}

/**
 * Returns a human-readable relative duration string for a record's time column.
 * Reacts to the `now` tick so values update without a page reload.
 */
function durationLabel(rec: QrHistoryRecord): string {
  // Reference `now.value` so Vue tracks the dependency and re-renders on each tick
  const _ = now.value

  if (rec.status === 'paid' && rec.paidAt) {
    return `Paid ${timeAgo(rec.paidAt)}`
  }
  if (rec.status === 'expired') {
    return `Expired ${timeAgo(rec.expiresAt)}`
  }
  if (rec.status === 'pending') {
    const diffMs = new Date(rec.expiresAt).getTime() - now.value
    if (diffMs <= 0) return 'Expiring nowâ€¦'
    return `Expires in ${formatDuration(diffMs)}`
  }
  return `Exp. ${formatDate(rec.expiresAt)}`
}

function durationClass(rec: QrHistoryRecord): string {
  if (rec.status === 'expired') return 'text-rose-400 font-medium'
  if (rec.status === 'paid')    return 'text-emerald-400'
  if (rec.status === 'pending') {
    const diffMs = new Date(rec.expiresAt).getTime() - now.value
    return diffMs < 60_000 ? 'text-amber-400 font-medium' : 'text-gray-500'
  }
  return 'text-gray-600'
}

/** "5 minutes ago", "2 hours ago", "3 days ago" */
function timeAgo(iso: string | null | undefined): string {
  if (!iso) return ''
  const diffMs  = now.value - new Date(iso).getTime()
  const diffSec = Math.abs(Math.floor(diffMs / 1000))

  if (diffSec < 60)        return `${diffSec}s ago`
  if (diffSec < 3600)      return `${Math.floor(diffSec / 60)}m ago`
  if (diffSec < 86400)     return `${Math.floor(diffSec / 3600)}h ago`
  return `${Math.floor(diffSec / 86400)}d ago`
}

/** "9m 30s", "1h 5m", "2d 3h" â€” for countdown-style remaining time */
function formatDuration(ms: number): string {
  const totalSec = Math.floor(ms / 1000)
  if (totalSec < 60)       return `${totalSec}s`
  if (totalSec < 3600) {
    const m = Math.floor(totalSec / 60)
    const s = totalSec % 60
    return s > 0 ? `${m}m ${s}s` : `${m}m`
  }
  if (totalSec < 86400) {
    const h = Math.floor(totalSec / 3600)
    const m = Math.floor((totalSec % 3600) / 60)
    return m > 0 ? `${h}h ${m}m` : `${h}h`
  }
  const d = Math.floor(totalSec / 86400)
  const h = Math.floor((totalSec % 86400) / 3600)
  return h > 0 ? `${d}d ${h}h` : `${d}d`
}

// â”€â”€ Inline QR modal â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

const activeDetail  = ref<PaymentQrDetail | null>(null)
const showQrModal   = ref(false)
const isLoadingQr   = ref(false)
const loadingQrId   = ref<string | null>(null)

async function openQrForRecord(qrId: string) {
  isLoadingQr.value = true
  loadingQrId.value = qrId
  try {
    const detail = await paymentsApi.getQr(qrId)
    if (detail) {
      activeDetail.value = detail
      showQrModal.value  = true
    }
  } finally {
    isLoadingQr.value = false
    loadingQrId.value = null
  }
}

function handleQrClose() {
  showQrModal.value  = false
  activeDetail.value = null
}

function handleQrRegenerate() {
  if (!activeDetail.value) return
  const orderId = activeDetail.value.orderId
  handleQrClose()
  void router.push(`/payments?regenerate=${orderId}`)
}

function handleQrPaid() {
  if (activeDetail.value) {
    // Optimistically reflect paid status in the table row
    const rec = records.value.find(r => r.qrId === activeDetail.value!.qrId)
    if (rec) rec.status = 'paid'
  }
  handleQrClose()
}

onMounted(() => {
  void load()
  clockTimer = setInterval(() => { now.value = Date.now() }, 30_000)
})

onUnmounted(() => {
  if (clockTimer !== null) clearInterval(clockTimer)
})
</script>
