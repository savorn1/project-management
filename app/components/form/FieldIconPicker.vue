const containerRef = ref<HTMLElement | null>(null)
const isOpen = ref(false)
const search = ref('')
const panelStyle = ref<Record<string, string>>({})

const allIcons = computed(() => props.icons ?? BUILT_IN_ICONS)

const filteredIcons = computed(() => {
  const q = search.value.toLowerCase()
  return q ? allIcons.value.filter(i => i.name.toLowerCase().includes(q)) : allIcons.value
})

function iconPath(name: string) {
  return allIcons.value.find(i => i.name === name)?.path ?? ''
}

function computePosition() {
  if (!containerRef.value) return
  const rect = containerRef.value.getBoundingClientRect()
  const panelH = 360
  let top = rect.bottom + 4
  if (top + panelH > window.innerHeight - 8) top = rect.top - panelH - 4
  if (top < 8) top = 8
  let left = rect.left
  if (left + 320 > window.innerWidth - 16) left = window.innerWidth - 320 - 16
  if (left < 8) left = 8
  panelStyle.value = { top: `${top}px`, left: `${left}px` }
}

function toggle() {
  if (props.disabled) return
  if (isOpen.value) { isOpen.value = false; return }
  computePosition()
  isOpen.value = true
  search.value = ''
}

function select(name: string) {
  emit('update:modelValue', name)
  isOpen.value = false
}

function onKeydown(e: KeyboardEvent) {
  if (e.key === 'Escape' && isOpen.value) isOpen.value = false
}

onMounted(() => document.addEventListener('keydown', onKeydown))
onUnmounted(() => document.removeEventListener('keydown', onKeydown))
